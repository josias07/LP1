    public void cargar_datos()
    {
        tbproducto = clsp.reporteProducto();
        for(IdProducto idp1 : tbproducto){
            modelo.addRow(new Object[]{idp1.getId(),
                                       // idp1.getNombre(),
                                       // idp1.getDireccion(),
                                       // idp1.getRuc(),
                                        idp1.getProducto(),
                                        idp1.getCantidad(),
                                        idp1.getPrecio(),
                                        idp1.getSub_total()
                });
        }
        
    }
